#include "main.h"
  
/**
* print_alphabet - Prints the alphabet in lowercase
*
* Return: Always 0 (Success)
*/
void print_alphabet_10(void)
{
          int i;
	  int j;
          for (j = 1; i <= 10; i++)
          {
		  for (j = 1; j <= 10; i++)
		  {
			  _putchar(j);
		  }
		  _putchar('\n');
          }
}
